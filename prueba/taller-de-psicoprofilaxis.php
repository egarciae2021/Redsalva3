			<p>¿Qué es la Psicoprofiláxis Obstétrica?</p>
			
						<p>Es la preparación integral de la gestante que le permite desarrollar hábitos y comportamientos saludables, una actitud positiva frente al embarazo, parto, puerperio y el recién nacido. Convirtiendo este proceso en una experiencia feliz y saludable, tanto para ella como para su bebé y su entorno familiar.</p>
