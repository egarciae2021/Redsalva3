			<p>Las personas con sudoración excesiva o hiperhidrosis parecen tener glándulas sudoríparas hiperactivas. La sudoración incontrolable puede llevar a una molestia significativa, tanto física como emocional.</p>
			<p>Cuando la sudoración excesiva afecta las manos, pies y axilas, se llama hiperhidrosis primaria o focal. En la mayor parte de los casos, no se puede encontrar la causa. Parece ser hereditario.</p>
			<p>Si la sudoración sucede como resultado de otra afección médica, se llama hiperhidrosis secundaria. La sudoración puede presentarse en todo el cuerpo o solo en una zona.</p>
