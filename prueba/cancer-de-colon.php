			<p><span class="u-dropcap g-color-topbar-montesur g-mr-20 g-mb-10">E</span>l intestino grueso es el último tramo del tubo digestivo. Tiene una longitud aproximada de 1,5 metros y se extiende desde el final del intestino delgado hasta el ano.</p>
			<p>La mayoría de los cánceres colorrectales aparecen sobre un pólipo existente años antes en la mucosa del colon o del recto, que con el paso del tiempo y debido a la acción de distintos agentes sobre él, evoluciona a un tumor maligno.</p>
			<p>¿Qué son los pólipos?</p>
			<p> Los pólipos son pequeños tumores benignos que aparecen en la mucosa del colon y del recto. Suelen ser bastante frecuentes en la población de más de 50 años. Cuando se observa un pólipo al microscopio, se aprecia que existen varios tipos diferentes: </p>
			
			<ul>
				<li>Hiperplásicos: son los más frecuentes y no malignizan. </li>
				<li>Inflamatorios: Como los anteriores, tampoco poseen capacidad de malignizar.</li>
				<li>Adenomatosos: estos pólipos poseen un potencial de malignidad, que depende de su tamaño y número. No todos los pólipos adenomatosos malignizan, tan sólo lo hacen un 10% de los mismos.</li>
			</ul>
			<p>Tratamiento de los pólipos</p>
			<p>Generalmente, el tratamiento de los pólipos consiste en la extirpación de los mismos (polipectomía) durante la realización de la colonoscopia. </p>
			<p><strong> SÍNTOMAS DEL CÁNCER COLORRECTAL</strong> </p>
			<p>Como todos los tumores malignos, el cáncer colorrectal produce una serie de síntomas, que pueden variar en función de su localización dentro del intestino grueso: </p>
			<ul>
				<li>Sangre en las heces: Es uno de los síntomas más frecuentes del cáncer colorrectal. Puede tratarse de sangre roja, más frecuente en tumores de recto, sigma y colon descendente, o de sangre negra, que se mezcla con las heces dando lugar a deposiciones de color negro también llamadas melenas. Las melenas aparecen con más frecuencia cuando el tumor está situado en el colon ascendente. </li>
				<li>Cambio en el ritmo de las deposiciones: Aparece diarrea o estreñimiento en personas con ritmo intestinal previo normal, aunque, lo más frecuente es que se intercalen periodos de estreñimiento con periodos de diarrea. </li>
			</ul>
			<p><strong> DIAGNÓSTICO DE CÁNCER COLORRECTAL:</strong></p>
			<p>Es necesario realizar una colonoscopia: La colonoscopia es una prueba que permite visualizar la mucosa de todo el colon y el recto a través de un tubo largo y flexible (endoscopio) que se introduce por el ano.</p>
