			<p><span class="u-dropcap g-color-topbar-montesur g-mr-20 g-mb-10">E</span>l cáncer de estómago es un tipo de cáncer que se forma en los tejidos que revisten el estómago. Estos tipos de cáncer se llaman adenocarcinomas y representan el 90 % de los casos de cáncer de estómago.</p>
			<p>En todo el mundo, el cáncer de estómago es más habitual en Asia oriental, América del Sur y Europa oriental; es menos frecuente en Europa occidental, aunque es el quinto más frecuente del continente. Su frecuencia en varones es aproximadamente el doble que en las mujeres. Suele diagnosticarse entre los 60 y 80 años. </p>
			<p><strong>¿QUÉ CAUSA EL CÁNCER DE ESTÓMAGO?</strong></p>
			<p> Hoy en día no está claro por qué se produce el cáncer de estómago, aunque se han identificado algunos factores de riesgo. Los principales factores de riesgo de cáncer de estómago son: </p>
			<ul>
				<li>Factores ambientales: Helicobacter pylori o H. pylori es una bacteria que puede residir en el estómago y provocar inflamación crónica o úlceras</li>
				<li>Estilo de vida: </li>
				<ul>
					<li>Nutrición: 
						<ul>
							<li>Una ingesta elevada de sal, incluyendo los salazones (como alimentos ahumados o conservados en sal), aumenta en gran medida el riesgo de aparición de cáncer de estómago. La presencia de sal aumenta la probabilidad de aparición de una infección con H. pylori y también parece agravar el efecto de la infección. </li>
							<li>Una ingesta elevada de alimentos que contengan nitratos o nitritos, como la carne en conserva, puede aumentar el riesgo de aparición de cáncer de estómago. </li>
							<li>Comer frutas y vegetales que contengan las vitaminas A y C ha demostrado proteger de forma significativa contra la aparición del cáncer de estómago.</li>
						</ul>
					</li>
					<li>Tabaco: La tasa de cáncer de estómago casi se duplica en los fumadores. </li>
					<li>Ocupación: Los trabajadores en los sectores del carbón, metal y caucho parecen presentar un riesgo ligeramente mayor de aparición de cáncer de estómago.</li>
					<li>Algunos estudios han demostrado que las personas que realizan mucha actividad física pueden reducir su riesgo de aparición de cáncer de estómago a la mitad. </li>
				</ul>
				<li>Factores que no pueden modificarse: 
					<ul>
						<li>Algunos trastornos genéticos pueden aumentar el riesgo de aparición del cáncer de estómago </li>
					</ul>
				</li>
			</ul>
			<p><strong>¿CÓMO SE DIAGNOSTICA EL CÁNCER DE ESTÓMAGO? </strong></p>
			<p>Puede sospecharse cáncer de estómago en diversas circunstancias. Por desgracia, las señales suelen ser vagas y muy comunes, pueden indicar muchos otros trastornos médicos. En la fase temprana, el cáncer de estómago no causa ningún síntoma. Por lo tanto, con frecuencia no se sospecha tumor gástrico. Por el número elevado de casos nuevos de cáncer de estómago, se propone un cribado (screening) para todas las personas a los 50 años con una Endoscopia.</p>
