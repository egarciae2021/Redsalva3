			<p><span class="u-dropcap g-color-topbar-montesur g-mr-20 g-mb-10">L</span>a enfermedad por reflujo gastroesofágico (ERGE) se define como una condición que se desarrolla cuando el reflujo del contenido gástrico causa síntomas molestos para el paciente y/o complicaciones. Se trata de una enfermedad muy prevalente, y con impacto significativo sobre la calidad de vida relacionada con la salud. </p>
			<p><strong>Sintomas</strong></p>
			<p>Los síntomas típicos de ERGE son la pirosis (sensación de quemazón que surge del estomago  o bajo torax hacia el cuello) y la regurgitación ácida, de tal forma que su presencia es un criterio diagnóstico de la enfermedad, especialmente si se alivian con tratamiento antisecretor.</p>
			<p>La disfagia(  La disfagia se refiere a la dificultad para deglutir) y la odinofagia son síntomas de alarma que obligan a la realización de una endoscopia. Cuando los síntomas típicos no mejoran con el tratamiento o si existen otros síntomas menos específicos, como hipo, náuseas, dolor torácico, sensación de globo o manifestaciones extraesofágicas, puede ser necesaria la realización de pruebas complementarias, como la endoscopia y la pH-impedanciometría esofágica. </p>
			<p><strong>Tratamiento</strong></p>
			<p>Modificaciones del estilo de vida  y Tratamiento farmacológico. El objetivo básico del tratamiento farmacológico de la ERGE es reducir la exposición del esófago al contenido gástrico. </p>
