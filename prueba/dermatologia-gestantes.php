			<p><strong>MANCHAS:</strong><br>Para prevenir o disminuir estas "manchas" se pueden utilizar sustancias despigmentantes compatibles con el embarazo. Normalmente, las manchas que más preocupan a las pacientes son las localizadas en la cara, que aparecen en el segundo trimestre en el 75% de las embarazadas y puede persistir en más de un 30% de los casos. Estas manchas, llamadas cloasma, responden bien a tratamientos con hidroquinona y tretinoína tras la gestación.</p>
			<p><strong>ESTRÍAS DE DISTENSIÓN </strong><br>Aparecen en casi todas las mujeres durante la gestación, sobre todo en el abdomen, nalgas, mamas y muslos. Suele existir una predisposición familiar y se pueden minimizar evitando subidas bruscas de peso, hidratando correctamente la piel y aplicando cremas con derivados de la vitamina A tras el parto.</p>
