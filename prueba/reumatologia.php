			<p><span class="u-dropcap g-color-topbar-montesur g-mr-20 g-mb-10">N</span>os especializamos en el diagnóstico y tratamiento de las enfermedades que afectan a los huesos, articulaciones y tejidos conectivos del organismo. Entre las enfermedades más comunes tenemos: </p>
			<p><strong>Enfermedades Difusas del Tejido Conectivo:</strong></p>
			<ul>
				<li>ARTRITIS REUMATOIDE<br>Enfermedad inflamatoria de origen inmune que destruye las articulaciones. El diagnóstico precóz, previene el daño articular.</li>
				<li>ESCLEROSIS SISTÉMICA<br>Enfermedad autoinmune que lleva a fibrosis en tejidos; como piel, pulmones, sistema digestivo. Actualmente se detecta precozmente.</li>
				<li>LUPUS ERITEMATOSO SITÉMICO<br>Enfermedad autoinmune con afectación de riñón, pulmón, vasos sanguíneos, entre otros. El no control pone en riesgo la vida.</li>
				<li>VASCULITIS<br>Inflamación de vasos sanguíneos con implicancias muy serias para la salud. El pronóstico favorable depende de su detección precóz.</li>
			</ul>
			<p><strong>Enfermedades Metabólicas:</strong></p>
			<ul>
				<li>GOTA<br>Ocurre cuando se acumulan cristales de urato en una articulación, lo que causa inflamación y dolor intenso en un ataque de gota. Los cristales de urato pueden formarse si tienes niveles altos de ácido úrico en la sangre.</li>
			</ul>
			<p><strong>Enfermedades Degenerativas:</strong></p>
			<ul>
				<li>ASTROSIS<br>Es un trastorno crónico que causa alteraciones en el cartílago y los tejidos circundantes, y se caracteriza por dolor, rigidez y pérdida de la función. La artritis causada por la afectación del cartílago articular y los tejidos circundantes se vuelve muy frecuente con el envejecimiento.</li>
				<li>OSTEOPOROSIS<br>Es una enfermedad que se caracteriza por una disminución de la densidad de los huesos a causa de la pérdida del tejido óseo normal; con la consiguiente aparición de fracturas.</li>
			</ul>
			<p><strong>Afectaciones Musculo esqueléticas:</strong></p>
			<ul>
				<li>HOMBRO DOLOROSO<br>Se define como aquel dolor que se sitúa en la región del hombro y aparece con algunos movimientos del brazo. Es una de las consultas médicas más frecuentes, y llega a afectar al 25% de la población en algún momento de la vida.</li>
				<li>FIBROMIALGIA<br>Es una afección crónica que causa dolor en todo el cuerpo, fatiga y otros síntomas. Las personas que la padecen pueden ser más sensibles al dolor que aquellas que no. Esto se conoce como percepción anormal del dolor.</li>
				<li>LUMBALGIA<br>Dolor que se producen en la zona baja de la espalda entre las vértebras L-1 y L-5. Dicho dolor se puede producir por diferentes razones entre las que destacan: Lesiones previas y acumulación de tensión en la zona.</li>
				<li>SINDROME MIOFASCIAL<br>Es un trastorno por dolor crónico. Con esta afección, la presión ejercida en los puntos sensibles de los músculos (puntos desencadenantes del dolor) provoca dolor en el músculo y, a veces, en partes del cuerpo que, aparentemente, no están conectadas.</li>
			</ul>
