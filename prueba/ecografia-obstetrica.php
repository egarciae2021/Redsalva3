			<p class="text-primary g-font-weight-700">III. DENOMINACIÓN Y DESCRIPCIÓN ESTANDARIZADA DE LOS SERVICIOS DE ECOGRAFÍA UMMF</p>


			<p><span class="text-primary g-font-weight-300">ECOGRAFÍAS ESTANDAR (BÁSICAS O BAJA COMPLEJIDAD)</span>:  Son las ecografías que se realizan desde hace muchos años y por ser limitadas están reemplazándose por las ecografías especializadas detalladas. Aún son realizadas por algún motivo específico a aquellas gestantes que ya han pasado al menos 3 controles ecográficos especializados (Ecografía Genética, Morfológica y Doppler 3er trimestre) y hayan resultado normales, de bajo riesgo o sin complicaciones.</p>

			<p>Estas son:</p>

			<p><strong>– Ecografía Obstétrica Básica del 1er Trimestre:</strong> Precisión de edad, biometría y peso, actividad cardiaca, evaluación anatómica básica limitada. No incluye marcadores de síndromes cromosómicos/genéticos ni Doppler ni precisión del riesgo de Preeclampsia. Se realiza menor de 14 semanas.</p>

			<p><strong>– Ecografía Obstétrica Básica del 2do Trimestre:</strong> Precisión de edad, biometría y peso, actividad cardiaca, ubicación de Placenta, estado de líquido y evaluación anatómica básica limitada. No incluye marcadores de síndromes cromosómicos/genéticos ni Doppler ni precisión del riesgo de Preeclampsia ni medición de longitud cervical para la predicción de Parto pretérmino. Se realiza entre 15-27 semanas</p>

			<p><strong>– Ecografía Obstétrica Básica del 3er Trimestre:</strong> Biometría y peso fetal, posición y presentación, actividad cardiaca, estado del líquido y ubicación de placenta y grado, evaluación anatómica básica limitada. No incluye marcadores de síndromes cromosómicos/genéticos ni Doppler ni precisión del riesgo de Preeclampsia ni medición de la longitud cervical para la predicción de Parto pretérmino. Se realiza entre las 28-41 semanas.</p>

			<p>Dado su carácter limitado, éstas permiten determinar necesidad de evaluación complementaria y la referencia a unidades especializadas de Medicina Materno Fetal.</p>

			<p><span class="text-primary g-font-weight-300">ECOGRAFÍAS ESPECIALIZADAS (DETALLADAS O ALTA COMPLEJIDAD):</span>:  Son las ecografías que debe realizarse toda gestante como un concepto moderno de calidad de control del embarazo. En manos experimentadas y en momentos específicos permiten la detección certera y oportuna de complicaciones, anomalías, riesgos de algún evento adverso materno/fetal y la necesidad de algún manejo o terapia fetal. Son el fundamento de la Nueva Pirámide de control Prenatal. Usan múltiple abordaje (transabdominal y/o transvaginal) y múltiples modos de imagen con la mejor tecnología y resolución (M + 2D + Doppler + 7D).</p>

			<p><strong>1. DE TAMIZAJE (A todas las gestantes rutinariamente)</strong></p>

			<p><strong>– Ecografía Genética Doppler 7D:</strong> Agregamos evaluación anatómica detallada y de marcadores de síndromes cromosómicos o genéticos, Doppler de vasos fetales y maternos (Arterias Uterinas) para la predicción del riesgo de Preeclampsia o de Restricción del crecimiento Fetal (RCF) y primer momento para la identificación de gestaciones de alto y bajo riesgo. Se realiza entre las 11-14 semanas. Usamos 2D + Doppler + 7D</p>

			<p><strong>– Ecografía Morfológica Doppler 7D:</strong> Agregamos evaluación anatómica detallada y de marcadores de síndromes cromosómicos o genéticos, Doppler de vasos fetales y maternos (Arterias Uterinas) para la predicción del riesgo de Preeclampsia o de Restricción del crecimiento Fetal (RCF), medición de longitud cervical para la predicción de Parto pretérmino y segundo momento para la identificación de gestaciones de alto y bajo riesgo. Se realiza entre las 20-24 semanas. Usamos 2D + Doppler + 7D</p>

			<p>Ante el hallazgo de alguna condición anormal en estas ecografías debe derivarse a ECOGRAFÍA ESPECIALIZADA DE ALTA COMPLEJIDAD para precisar diagnósticos, pronóstico, vigilancia, pruebas complementarias y/o manejo. </p>

			<p><strong>2. DE ALTA COMPLEJIDAD (A gestantes o fetos con algún riesgo, anomalía, sospecha o complicación específica y realizadas por médicos con experiencia en medicina materno fetal)</strong></p>

			<p><strong>– Ecografía Doppler-Crecimiento-Bienestar-7D del 3er Trimestre:</strong> Agregamos reevaluación anatómica detallada y marcadores tardíos de síndromes cromosómicos o genéticos, evaluación del bienestar o Perfil Biofísico, Doppler de vasos fetales para la evaluación cardiovascular (Arteria Umbilical, ACM, Ductus Venoso, indicadores Doppler de función cardiaca) y maternos (Arterias Uterinas) para la predicción del riesgo de Preeclampsia o de Restricción del crecimiento Fetal (RCF). Esta es de importancia en el diagnóstico, vigilancia y manejo de embarazo múltiple, sospecha de acretismo, malformaciones o complicaciones materno fetales y en la detección del riesgo de muerte fetal. Se realiza en > 24 semanas. Usamos 2D + Doppler + 7D</p>

			<p><strong>– Ecocardiografía-Doppler-7D:</strong> Agregamos evaluación anatómica y funcional detallada del corazón y sistema. Se indica cuando la anomalía afecta principalmente al corazón e incluye también una evaluación total del feto. Se realiza entre las 20-38 semanas.Usamos modo M + 2D + Doppler + 7D.</p>

			<p><strong>– Neurosonografía-Doppler-7D:</strong> Agregamos evaluación anatómica y/o vascular detallada del Sistema Nervioso Central. Se indica cuando la anomalía afecta principalmente al cerebro o columna e incluye también una evaluación total del feto. Se realiza entre las 20-38 semanas.Usamos 2D + Doppler + 7D.</p>

			<p>Estas últimas pueden brindar información del pronóstico, vigilancia, manejo, necesidad y aporte de pruebas complementarias y estimaciones del manejo posnatal.</p>

