			<p><span class="u-dropcap g-color-topbar-montesur g-mr-20 g-mb-10">L</span>a Hernioplastía es un procedimiento para corregir una hernia cuando la pared muscular es débil, ya que tras retirarla, el saco busca la reparación de la pared abdominal. El tipo de hernia que usted tenga depende de su ubicación:</p>
			<ul>
				<li><strong>Hernia crural:</strong><br> Es una protuberancia en la parte superior del muslo, justo debajo de la ingle. Este tipo es más común en las mujeres que en los hombres.</li>
				<li><strong>Hernia hiatal:</strong><br> Se presenta en el límite de la cavidad toráxica abdominal a través del musculo diepopo.</li>
				<li><strong>Hernia quirúrgica o eventración:</strong><br> Puede darse a través de una cicatriz si el paciente ha tenido una cirugía abdominal en el pasado.</li>
				<li><strong>Hernia umbilical:</strong><br> Es una protuberancia alrededor del ombligo. Sucede cuando el músculo alrededor del ombligo no se cierra completamente después del nacimiento.</li>
				<li><strong>Hernia inguinal:</strong><br> Es una protuberancia en la ingle que puede bajar hasta el escroto. Este tipo es más común en los hombres.</li>
			</ul>
			<p>En la hernioplastía se utiliza anestesia general o epidural de acuerdo al tipo de hernia. Su duración es de una a dos horas dependiendo el tipo de hernia y su recuperación es de una a tres semanas.</p>
