			<p><span class="u-dropcap g-color-topbar-montesur g-mr-20 g-mb-10">L</span>a actividad física y la alimentación son claves en el tratamiento de la diabetes, pero cada paciente es único, tiene diferentes requerimientos, elecciones, prioridades y valores que deben ser considerados, así que su tratamiento debe ser personalizado.</p>
			<p><strong>¿Qué es la diabetes?</strong></p>
			<p>Es una enfermedad metabólica en la que los niveles de glucosa en sangre están elevados. Este aumento constante se debe a que el páncreas de la persona afectada reduce la producción de insulina, una hormona que se encarga de transformar la glucosa en combustible para que el cuerpo pueda funcionar de correctamente.</p>
			<p>Un paciente puede presentar uno de los dos tipos de diabetes siguientes:</p>
			<ul>
				<li><strong>Diabetes tipo I o juvenil:</strong><br> Diagnosticada principalmente en niños o adultos jóvenes, es una condición en la que el cuerpo no crea suficiente cantidad de insulina o casi nada.</li>
				<li><strong>Diabetes tipo II o del adulto:</strong><br> Es el tipo de diabetes más común, se desarrolla en pacientes adultos y personas con obesidad. El tratamiento de la diabetes implica un cambio en el estilo de vida del paciente, sin embargo, no es un cambio complicado si se cuenta con la asesoría adecuada que le brinde bienestar y le asegure un control de la enfermedad.</li>
			</ul>
			<p>El tratamiento de la diabetes implica un cambio en el estilo de vida del paciente, sin embargo, no es un cambio complicado si se cuenta con la asesoría adecuada que le brinde bienestar y le asegure un control de la enfermedad.</p>
			<p><strong>¿Cuáles son los síntomas?</strong></p>
			<p>La diabetes es una enfermedad silenciosa en sus inicios y suele manifestarse como cansancio, lo mejor es prevenir si sufres de obesidad o si tienes padres y hermanos que han sufrido la enfermedad.</p>
			<p>Los síntomas clásicos de la diabetes del tipo II, también conocida como diabetes mellitus, son sed y apetito incrementado, frecuencia y volumen urinario incrementado y pérdida de peso. Todos estos síntomas nos indican que el paciente se encuentra en una crisis hiperglicémica, que es glucosa mayor a 250mg/dl.</p>
